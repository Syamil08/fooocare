package com.example.fooocare.Model;

public class Sepakbola {
    int berat, pengali;

    public Sepakbola(int berat, int pengali) {
        this.berat = berat;
        this.pengali = pengali;
    }

    public int getBerat() {
        return berat;
    }

    public void setBerat(int berat) {
        this.berat = berat;
    }

    public int getPengali() {
        return pengali;
    }

    public void setPengali(int pengali) {
        this.pengali = pengali;
    }
}
